  end

  def generate_initializer
    template "stripe.rb.erb", "config/initializers/stripe.rb"
  end

  def add_routes
    route_content = <<~ROUTES
      resources :payments, only: [] do
        member do
          get :pay
          post :pay
          get :success
          get :failure
        end
      end
      post '/webhooks/stripe', to: 'payments#webhook'
    ROUTES

    route route_content
  end

